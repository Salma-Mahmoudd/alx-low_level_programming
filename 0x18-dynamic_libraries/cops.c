#include "cops.h"
/**
 * add - function that sum two numbers
 * @a: first number
 * @b: second number
 * Return: the result.
 **/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that substracte two numbers
 * @a: first number
 * @b: second number
 * Return: the result.
 **/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that multiple two numbers
 * @a: first number
 * @b: second number
 * Return: the result.
 **/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that divide two numbers
 * @a: first number
 * @b: second number
 * Return: the result.
 **/
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that module two numbers
 * @a: first number
 * @b: second number
 * Return: the result.
 **/
int mod(int a, int b)
{
	return (a % b);
}
